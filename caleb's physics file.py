class Physics:

    def calc_average_speed(self, d, t):
        return d/t

    def calc_newtons_second_law(self, m, a):
        return m*a

    def calc_power(self, w, t):
        return w/t

    def calc_workdone(self, f, d):
        return f*d

    def calc_momentum(self, M, V):
        return M*V